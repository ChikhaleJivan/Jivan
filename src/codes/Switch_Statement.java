package codes;
public class Switch_Statement {
public static void main(String[] args) {
		char grade='y';
		switch(grade)
		{
		case 'A':
			System.out.println("it is grade A::"+grade);
			break;
		case 'B':
			System.out.println("it is grade B::"+grade);
			break;
		case 'C':
			System.out.println("it is grade C::"+grade);
			break;
		case 'D':
			System.out.println("it is grade A::"+grade);
			break;
		default:
			System.out.println("not matching");
}
}
}
