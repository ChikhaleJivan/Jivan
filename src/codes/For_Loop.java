package codes;
public class For_Loop 
{
	public static void main(String[] args) 
	{
		for(int i=1;i<=10;i++)
			{
			System.out.println("ranga");
			}
		System.out.println("========================");
		for(int i=1;i<=10;i+=2)
			{
			System.out.println("ranga");
			}
		System.out.println("========================");
		for(int i=10;i>=1;i--)
			{
			System.out.println(i);
			}
	}
}
